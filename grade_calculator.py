score = int(input("Please enter score: "))

if 80 <= score <= 100:
    print("A+")
elif 70 <= score < 80:
    print("B")
elif 60 <= score < 70:
    print("C")
elif 50 <= score < 60:
    print("D")
elif 0 <= score < 50:
    print("F")
else:
    print("score error")

